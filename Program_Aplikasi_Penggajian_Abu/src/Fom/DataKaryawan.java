    private void tabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMouseClicked
        // TODO add your handling code here:
        table();
            if(evt.getClickCount()==2){
                int jawab = JOptionPane.showOptionDialog(this,
                "Proses Data Gaji Karyawan?",
                "Proses",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null, null, null );
            if (jawab == JOptionPane.YES_OPTION){
                DataPenggajian Laund = new DataPenggajian();
                Laund.setVisible(true);
                int selectedRow = tabelData.getSelectedRow();
                DefaultTableModel dataTabel = (DefaultTableModel) tabelData.getModel();

                DataPenggajian.nikTF.setText(dataTabel.getValueAt(selectedRow, 1).toString());
                DataPenggajian.namaTF.setText(dataTabel.getValueAt(selectedRow, 2).toString());
                DataPenggajian.jabatanTF.setText(dataTabel.getValueAt(selectedRow, 14).toString());
                DataPenggajian.kodeJabatanTF.setText(dataTabel.getValueAt(selectedRow, 15).toString());
                DataPenggajian.jumlahanakTF.setText(dataTabel.getValueAt(selectedRow, 10).toString());
                DataPenggajian.gajiPokokTF.setText(dataTabel.getValueAt(selectedRow, 16).toString());
                DataPenggajian.tJabatanTF.setText(dataTabel.getValueAt(selectedRow, 17).toString());
                DataPenggajian.gLemburTF.setText(dataTabel.getValueAt(selectedRow, 18).toString());
            }
        }
    }//GEN-LAST:event_tabelDataMouseClicked

